const calCoordinate = (keyInput: string[], board: number[]): number[] => {
  // ÏïÑÏù¥ÎîîÏñ¥üí°
  // resultÏùò Í∞Å ÏõêÏÜåÍ∞Ä boardÎ•º Î≤óÏñ¥ÎÇòÎ©¥ ÏïàÎêòÍ∏∞ ÎïåÎ¨∏Ïóê
  // Math.floor(board[i] - 1 / 2)Î•º ÏÇ¨Ïö©Ìï¥ÏÑú
  // boardÏùò ÏµúÎåÄ [Í∞ÄÎ°ú, ÏÑ∏Î°ú] Í∏∏Ïù¥Î•º Íµ¨Ìï® (Ï†àÎåÄÍ∞íÏúºÎ°ú)

  // keyinput.lengthÎßåÌÅº Î°úÏßÅ Î∞òÎ≥µ
  // Ï£ºÏñ¥ÏßÑ Î∞©Ìñ•ÏúºÎ°ú ÏõÄÏßÅÏù∏ Ï¢åÌëúÍ∞íÏùÑ Î∞∞Ïó¥ Íµ¨Ï°∞ Ìï†ÎãπÏúºÎ°ú Îã§Î•∏ Î≥ÄÏàòÏóê ÏßÄÏ†ï
  // (ÎßàÏßÄÎßâÏóê boardÏóêÏÑú Î≤óÏñ¥ÎÇòÎäîÏßÄ ÏïÑÎãåÏßÄ Ï≤¥ÌÅ¨ÌïòÍ∏∞ ÏúÑÌï®)

  // Îî∞Î°ú Îã¥ÏïÑÎëî Ïó∞ÏÇ∞ Ïù¥ÌõÑÏùò Ï¢åÌëúÍ∞íÏùÑ boardÏùò Í∞ÄÎ°ú, ÏÑ∏Î°úÍ∏∏Ïù¥ÏôÄ ÎπÑÍµêÌï¥ÏÑú
  // ÎÑòÏñ¥Í∞Ñ Í∞íÏù¥ ÏóÜÎã§Î©¥ resultÏóê Î∞òÏòÅ
  // ÎÑòÏñ¥Í∞Ñ Í∞íÏù¥ ÏûàÎã§Î©¥ Î¨¥Ïãú -> Í∑∏ Îã§Ïùå Ï°∞Í±¥ Î∞òÎ≥µ
  const maxGaro = Math.floor((board[0] - 1) / 2);
  const maxSero = Math.floor((board[1] - 1) / 2);

  const delta: Record<string, number[]> = {
    up:    [0, 1],
    down:  [0, -1],
    left:  [-1, 0],
    right: [1, 0],
  };

  let x = 0, y = 0;

  for (let i = 0; i < keyInput.length; i++) {
    const [dx, dy] = delta[keyInput[i]]
    if (Math.abs(x + dx) <= maxGaro && Math.abs(y + dy) <= maxSero) {
      x = x + dx;
      y = y + dy;
    }
  }
  return [x, y];
}

